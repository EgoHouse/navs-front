// Funciones auxiliares para filtrar y organizar
export const getShishasByCategory = (category?: string): ShishaItem[] => {
  if (!category || category === 'todas') {
    return shishaData;
  }
  return shishaData.filter((shisha) => shisha.category === category);
};

export const getUniqueCategories = (): string[] => {
  const categories = shishaData
    .map((shisha) => shisha.category)
    .filter((category): category is string => category !== undefined);
  return [...new Set(categories)].sort();
};

export const getShishaById = (id: number): ShishaItem | undefined => {
  return shishaData.find((shisha) => shisha.id === id);
};
